export function blockColorRGB(
  id: number,
  meta: number
): readonly [number, number, number] {
  const c = COLORS[id]
  if (c) return c
  // Deterministic hash for unknown/modded block IDs
  let h = Math.imul(id ^ (id >>> 16), 0x45d9f3b)
  h ^= Math.imul(meta, 0x119de1f3)
  h ^= h >>> 15
  return [
    Math.floor(((h >>> 16) & 0xff) * 0.65 + 30),
    Math.floor(((h >>> 8) & 0xff) * 0.65 + 30),
    Math.floor((h & 0xff) * 0.65 + 30),
  ]
}
